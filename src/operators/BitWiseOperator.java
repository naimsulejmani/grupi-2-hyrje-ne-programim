package operators;

public class BitWiseOperator {
    public static void main(String[] args) {
        System.out.println(42 & 15);
        System.out.println(42 | 15);

        System.out.println(1 << 2);
        System.out.println(1 << 10);
        System.out.println(15 << 1);
        System.out.println(15 >> 1);
    }
}
